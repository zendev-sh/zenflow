//go:build example

// Condition - conditional step execution via CEL expressions.
// The security audit step runs only if the design output mentions
// authentication; the performance optimisation step runs only if tests
// pass. Demonstrates the `if` field on steps and CEL access to
// previous-step outputs.
// Run from the zenflow/ directory:
//	export GEMINI_API_KEY=...
//	go run -tags example ./examples/condition/
package main

import (
	"context"
	"fmt"
	"log"
	"os"

	"github.com/zendev-sh/goai/provider/google"
	"github.com/zendev-sh/zenflow"
)

func main() {
	wf, err := zenflow.LoadWorkflow("spec/v1/examples/condition.yaml")
	if err != nil {
		log.Fatal("load: ", err)
	}

	llm := google.Chat("gemini-2.0-flash", google.WithAPIKey(os.Getenv("GEMINI_API_KEY")))
	orch := zenflow.New(zenflow.WithModel(llm))
	defer orch.Close()

	result, err := orch.RunFlow(context.Background(), wf)
	if err != nil {
		log.Fatal("run: ", err)
	}

	fmt.Printf("run %q: status=%q duration=%s steps=%d\n",
		result.RunID, result.Status, result.Duration, len(result.Steps))
	if result.Summary != "" {
		fmt.Println()
		fmt.Println(result.Summary)
	}
}
